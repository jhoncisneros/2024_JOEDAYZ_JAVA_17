package pe.joedayz.samples;

public class PromocionNumerica {

    public static void main(String[] args) {

        int x = 1;
        long y = 3;

        var z = x * y;

        System.out.println(z);

        double a = 39.21;
        float b = 2.1f;

        var c = a + b;

        System.out.println(c);

        short xx = 10;
        short yy = 3;

        var zz = xx * yy;

        System.out.println(zz);

        short p = 14;
        float q = 13;
        double r = 30;
        var s = p * q / r;

        System.out.println(s);
    }
}
